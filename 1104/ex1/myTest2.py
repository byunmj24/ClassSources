# JAVA 숫자형 : 정수(int, byte, short, long), 실수(float, double)
# 하지만 파이썬에서는 정수형이 int 하나로 통합되어 있다. 실수는 float

a = 2700000000000000000000000000000000000000000000000000000000
print("a=%d"%a) # %d : decimal, 10진수를 의미
print("type(a):%s"%type(a)) # %s : 문자열로 표기함을 의미

b = 4.1
print("b=%d"%b)
print("type(b)=%s"%type(b))

print("a/b=%f"%(a/b))